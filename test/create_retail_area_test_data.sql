-- 新增店面功能测试数据
-- 用于测试新增店面用例

-- 清理可能存在的测试数据
DELETE FROM RETAIL_AREA WHERE AREA_ID IN (1001, 1002, 1003, 1004, 1005);
DELETE FROM AREA WHERE AREA_ID IN (1001, 1002, 1003, 1004, 1005);
COMMIT;

-- 插入测试用的区域数据（用于验证区域ID唯一性）
-- 先插入AREA表数据
INSERT INTO AREA (AREA_ID, ISEMPTY, AREA_SIZE, CATEGORY) VALUES (1001, 1, 50, 'RETAIL');
INSERT INTO AREA (AREA_ID, ISEMPTY, AREA_SIZE, CATEGORY) VALUES (1002, 1, 80, 'RETAIL');
INSERT INTO AREA (AREA_ID, ISEMPTY, AREA_SIZE, CATEGORY) VALUES (1003, 0, 120, 'RETAIL'); -- 添加 CATEGORY 值

-- 再插入RETAIL_AREA表数据（注意：不包含AREA_SIZE和ISEMPTY字段）
INSERT INTO RETAIL_AREA (AREA_ID, RENT_STATUS, BASE_RENT) VALUES (1001, '空置', 5000.00);
INSERT INTO RETAIL_AREA (AREA_ID, RENT_STATUS, BASE_RENT) VALUES (1002, '空置', 8000.00);
INSERT INTO RETAIL_AREA (AREA_ID, RENT_STATUS, BASE_RENT) VALUES (1003, '已租用', 12000.00);

COMMIT;

-- 验证数据插入成功
SELECT 'AREA表数据' as TABLE_NAME, AREA_ID, ISEMPTY, AREA_SIZE, NULL as BASE_RENT FROM AREA WHERE AREA_ID IN (1001, 1002, 1003)
UNION ALL
SELECT 'RETAIL_AREA表数据' as TABLE_NAME, AREA_ID, NULL as ISEMPTY, NULL as AREA_SIZE, BASE_RENT FROM RETAIL_AREA WHERE AREA_ID IN (1001, 1002, 1003)
ORDER BY TABLE_NAME, AREA_ID;

-- 联合查询验证完整数据
SELECT a.AREA_ID, a.ISEMPTY, a.AREA_SIZE, r.RENT_STATUS, r.BASE_RENT 
FROM AREA a 
JOIN RETAIL_AREA r ON a.AREA_ID = r.AREA_ID 
WHERE a.AREA_ID IN (1001, 1002, 1003);

-- 测试场景说明：
-- 1001: 空置的小型店面，用于测试正常创建流程
-- 1002: 空置的中型店面，用于测试不同规格店面
-- 1003: 已租用的大型店面，用于测试区域ID重复检查
-- 1004, 1005: 预留给新增店面测试使用

